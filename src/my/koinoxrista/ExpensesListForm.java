    public void updateExpensesTable() {
        Connection connection = SqliteConnection.getConnection();

        String query = "select * from expenses where building_id = ?";
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            pstmt = connection.prepareStatement(query);
            pstmt.setInt(1, _buildingId);
            rs = pstmt.executeQuery();
            DefaultTableModel dtm = buildTableModel(rs);
            this.expensesTable.setDefaultEditor(Object.class, null);
            this.expensesTable.setSelectionModel(new SingleListSelectionModel());
            this.expensesTable.setModel(dtm);
            dtm.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

    public void updateExpensesTable(String period) {
        Connection connection = SqliteConnection.getConnection();

        String query = "select * from expenses where building_id = ? and period like ?";
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            pstmt = connection.prepareStatement(query);
            pstmt.setInt(1, _buildingId);
            pstmt.setString(2, period);
            rs = pstmt.executeQuery();
            DefaultTableModel dtm = buildTableModel(rs);
            this.expensesTable.setDefaultEditor(Object.class, null);
            this.expensesTable.setSelectionModel(new SingleListSelectionModel());
            this.expensesTable.setModel(dtm);
            dtm.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

    public static DefaultTableModel buildTableModel(ResultSet rs)
            throws SQLException {

        ResultSetMetaData metaData = rs.getMetaData();

        // names of columns
        Vector<String> columnNames = new Vector<String>();
        columnNames.add("ID Εξόδου");
        columnNames.add("Κωδικός Πολυκατοικίας");
        columnNames.add("Κατηγορία");
        columnNames.add("Περιγραφή");
        columnNames.add("Περίοδος");
        columnNames.add("Ποσό");

        // data of the table
        int columnCount = metaData.getColumnCount();
        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();

            vector.add(rs.getInt("id"));
            vector.add(rs.getInt("building_id"));
            vector.add(Expense.getCategoryLiteral(rs.getInt("category")));
            vector.add(rs.getString("description"));
            vector.add(rs.getString("period"));
            vector.add(rs.getFloat("amount"));

            data.add(vector);
        }

        DefaultTableModel dtm = new DefaultTableModel(data, columnNames);

        return dtm;

    }

