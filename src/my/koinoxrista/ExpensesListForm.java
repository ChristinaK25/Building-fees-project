    private void editRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editRecordButtonActionPerformed
        int row = this.expensesTable.getSelectedRow();

        // Check if user has actually selected an apartment to edit
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Παρακαλώ επιλέξτε έξοδο για επεξεργασία.",
                    "Σφάλμα",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            // Get currently selected apartment ID
            int expense_id = (int) this.expensesTable.getModel().getValueAt(row, 0);
            ExpenseDetailsForm form = new ExpenseDetailsForm(expense_id, _buildingId, this);
            form.setVisible(true);
        }
    }//GEN-LAST:event_editRecordButtonActionPerformed

    private void deleteRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteRecordButtonActionPerformed
        int row = this.expensesTable.getSelectedRow();

        // Check if user has actually selected an expense to delete
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Παρακαλώ επιλέξτε έξοδο για διαγραφή.",
                    "Σφάλμα",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            int result = JOptionPane.showConfirmDialog(this,
                    "Είστε βέβαιοι πως επιθυμείτε να διαγράψετε το επιλεγμένο έξοδο;\n"
                    + "Η ενέργεια αυτή δεν μπορεί να αναιρεθεί.",
                    "Επιβεβαίωση Διαγραφής",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (result == 0) {
                int expense_id = (int) this.expensesTable.getModel().getValueAt(row, 0);
                String query = "delete from expenses where id = ?";
                Connection connection = SqliteConnection.getConnection();
                PreparedStatement pstmt = null;

                try {
                    pstmt = connection.prepareStatement(query);
                    pstmt.setInt(1, expense_id);
                    pstmt.executeUpdate();
                } catch (Exception ex) {
                    ex.printStackTrace();
                } finally {
                    try {
                        if (pstmt != null) {
                            pstmt.close();
                        }
                        if (connection != null) {
                            connection.close();
                        }
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }

                this.updateExpensesTable();
            }
        }
    }//GEN-LAST:event_deleteRecordButtonActionPerformed

