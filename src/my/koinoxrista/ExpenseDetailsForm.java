    public ExpenseDetailsForm(int expenseId, int buildingId, ExpensesListForm parent) {
        initComponents();
        _expenseId = expenseId;
        _buildingId = buildingId;
        _parent = parent;

        Connection connection = SqliteConnection.getConnection();
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        int category = 0;
        String description = "";
        String period = "";
        float amount = 0;

        if (_expenseId != 0) {
            String query = "select * from expenses where id = ?";
            try {
                pstmt = connection.prepareStatement(query);
                pstmt.setInt(1, _expenseId);
                rs = pstmt.executeQuery();
                while (rs.next()) {
                    category = rs.getInt("category");
                    description = rs.getString("description");
                    period = rs.getString("period");
                    amount = rs.getFloat("amount");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            } finally {
                try {
                    if (rs != null) {
                        rs.close();
                    }
                    if (pstmt != null) {
                        pstmt.close();
                    }
                    if (connection != null) {
                        connection.close();
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        }

        // Populate form with expense data
        this.categoryComboBox.setSelectedIndex(category);
        this.descriptionTextField.setText(description);
        this.periodTextField.setText(period);
        this.amountTextField.setText(amount + "");
    }

