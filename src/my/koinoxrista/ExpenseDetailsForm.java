    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Connection connection = SqliteConnection.getConnection();
        PreparedStatement pstmt = null;

        if (_expenseId == 0) {
            // Create new building

            String query = "insert into expenses (building_id, category, description, period, amount) values (?, ?, ?, ?, ?)";

            try {
                pstmt = connection.prepareStatement(query);
                pstmt.setInt(1, _buildingId);
                pstmt.setInt(2, this.categoryComboBox.getSelectedIndex());
                pstmt.setString(3, this.descriptionTextField.getText());
                pstmt.setString(4, this.periodTextField.getText());
                pstmt.setString(5, this.amountTextField.getText());
                pstmt.executeUpdate();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        } else {
            // Update new building
            String query = "update expenses set category = ?, description = ?, period = ?, amount = ? where id = ?";

            try {
                pstmt = connection.prepareStatement(query);
                pstmt.setInt(1, this.categoryComboBox.getSelectedIndex());
                pstmt.setString(2, this.descriptionTextField.getText());
                pstmt.setString(3, this.periodTextField.getText());
                pstmt.setString(4, this.amountTextField.getText());
                pstmt.setInt(5, _expenseId);
                pstmt.executeUpdate();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

        try {
            if (pstmt != null) {
                pstmt.close();
            }
            if (connection != null) {
                connection.close();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        _parent.updateExpensesTable();
        this.setVisible(false);
    }//GEN-LAST:event_saveButtonActionPerformed

